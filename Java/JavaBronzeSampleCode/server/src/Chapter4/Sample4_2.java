package Chapter4;

public class Sample4_2 {
    public static void main(String[] args) {
        // int num = 0;
        int num = 5; // num変数を 5 で初期化する

        while (num > 0) { // num の値が 0 より大きい間以下を繰り返す
            System.out.println("num の値は " + num + " です。");
            num--; // num の値から 1 減算
        }
    }
}
