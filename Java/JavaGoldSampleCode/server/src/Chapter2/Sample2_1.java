package Chapter2;

public class Sample2_1 {
    public static void main(String[] args) {
        System.out.println(255);        // 10進数: 255
        System.out.println(0377);       // 8 進数: 0377
        System.err.println(0xff);       // 16進数: 0xff
        System.out.println(0b101);      // 2 進数: 0b101
        System.out.println(12.33);      // 10進数表記
        System.out.println(3e4);        // 指数表記
        System.err.println('A');        // 1文字
        System.out.println('\u3012');   // Unicode
        System.out.println("Hello");    // 文字列
        System.out.println(true);       // 論理値
    }    
}
